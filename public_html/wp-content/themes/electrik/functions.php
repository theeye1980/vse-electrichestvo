		LEFT JOIN $wpdb->term_taxonomy tax ON (rel.term_taxonomy_id = tax.term_taxonomy_id)";
	$same_and = "AND tax.term_id = '$cat_id'
		AND tax.taxonomy = 'category'
		AND p.post_status = 'publish'
		AND p.post_type = 'post'";
